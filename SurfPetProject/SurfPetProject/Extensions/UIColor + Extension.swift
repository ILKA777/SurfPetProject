import UIKit

extension UIColor {
    
    static func buttonYellowGreen() -> UIColor {
        return #colorLiteral(red: 0.5647058824, green: 0.9098039216, blue: 0, alpha: 1)
    }
    
    static func buttonCustomBlue() -> UIColor {
        return #colorLiteral(red: 0.1490196078, green: 0.2352941176, blue: 1, alpha: 1)
    }
    
    static func buttonCustomBlack() -> UIColor {
        return #colorLiteral(red: 0.1921568627, green: 0.1921568627, blue: 0.1921568627, alpha: 1)
    }
    
    static func buttonCustomYellow() -> UIColor {
        return #colorLiteral(red: 1, green: 0.7215686275, blue: 0, alpha: 1)
    }
    
    static func buttonCustomGray() -> UIColor {
        return #colorLiteral(red: 0.9529411765, green: 0.9529411765, blue: 0.9607843137, alpha: 1)
    }
    
}
